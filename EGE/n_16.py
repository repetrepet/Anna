# Алгоритм вычисления значения функции F(n), где п - целое число, задан следующими соотношениями:
# F(n) = 1, если п<2
# F(n) = F(n-2) + F(n-1), если п≥ 2
# Чему равно значение выражения F(5)?

def f(n):
    # Базовый случай рекурсии: если аргумент меньше 2, функция возвращает 1.
    # Это условие останавливает дальнейшие рекурсивные вызовы и задает начальное значение.
    if n < 2:
        return 1
    # Рекурсивный случай: для аргументов >= 2 функция вызывает саму себя 
    # для значений (n-2) и (n-1), складывая их результаты согласно заданной формуле.
    elif n >= 2:
        return f(n - 2) + f(n - 1)

# Вычисляем и выводим значение функции для n = 5
print(f(5))


# Алгоритм вычисления значения функции F (n), где п - натуральное число, задан такими соотношениями:
# F(n) = n при п >= 7000
# F(n) = F(n +2) + n + 3, если п < 7000
# Чему равно значение выражения F(52) - F(56)?

from sys import setrecursionlimit
# Увеличиваем максимальную глубину рекурсии до 10000.
# Это необходимо, так как при вычислении F(52) или F(56) потребуется 
# несколько тысяч вложенных вызовов функции до достижения базового случая (n >= 7000).
setrecursionlimit(10000)

def f(n):
    # Базовый случай: когда аргумент достигает 7000 или больше, 
    # рекурсия завершается, и функция возвращает само значение n.
    if n >= 7000:
        return n
    # Рекурсивный шаг: если аргумент меньше 7000, функция вызывает себя для (n + 2),
    # постепенно увеличивая аргумент, и прибавляет к результату выражение (n + 3).
    else:
        return f(n + 2) + n + 3

# Вычисляем разность значений функции для аргументов 52 и 56 и выводим результат
print(f(52) - f(56))


# Алгоритм вычисления значения функции F(n), где n — натуральное число, задан такими соотношениями:
# • F(n) = 1 при n < 3
# • F(n) = F(n - 1) + n - 1 при n > 2 и при этом чётно
# • F(n) = F(n - 2) + 2n - 2, если n > 2 и при этом нечётно
# Чему равно значение выражения F(3048) − F(3045)?

from sys import setrecursionlimit
# Увеличиваем лимит глубины рекурсии, так как аргументы 3048 и 3045 велики,
# и алгоритму потребуется значительное количество шагов для сведения к базовому случаю.
setrecursionlimit(10000)

def f(n):
    # Базовый случай рекурсии: для всех значений n < 3 функция возвращает 1.
    if n < 3:
        return 1
    # Первый рекурсивный случай: если n > 2 и число чётное (делится на 2 без остатка),
    # значение вычисляется как сумма результата F(n - 1) и выражения (n - 1).
    elif n > 2 and n % 2 == 0:
        return f(n - 1) + n - 1
    # Второй рекурсивный случай: если n > 2 и число нечётное (остаток от деления на 2 не равен 0),
    # значение вычисляется как сумма результата F(n - 2) и выражения (2n - 2).
    elif n > 2 and n % 2 != 0:
        return f(n - 2) + 2 * n - 2

# Вычисляем разность значений функции для аргументов 3048 и 3045 и выводим итоговый результат
print(f(3048) - f(3045))
