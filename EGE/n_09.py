# В каждой строке электронной таблицы записаны четыре натуральных числа.
# Определите, сколько в таблице таких четвёрок, из которых можно выбрать три числа,
# которые не могут быть сторонами никакого треугольника, в том числе вырожденного
# (вырожденным называется треугольник, у которого сумма длин двух сторон равна длине третьей стороны).
#
# Пояснение к условию:
# Треугольник существует, если (a + b > c), где c - наибольшая сторона.
# "Не могут быть сторонами" (включая вырожденный) означает обратное условие: (a + b <= c).
# Нам нужно найти хотя бы одну тройку чисел из четырех, удовлетворяющую этому условию.

# a + b < c
# a + b < d

#  a  b  c  d
# [1, 2, 4, 7] +
# [4, 4, 4, 7] -

# Открываем файл с данными (предполагается, что файл лежит в той же папке)
f = open('data.txt')
count = 0

for line in f:
    # Считываем строку, разбиваем по пробелам и преобразуем в список целых чисел
    lst = [int(i) for i in line.split()]
    
    # Сортируем список по возрастанию.
    # После сортировки: lst[0] <= lst[1] <= lst[2] <= lst[3]
    lst.sort()
    
    # ЛОГИКА РЕШЕНИЯ:
    # Нам нужно выбрать ТРИ числа, которые НЕ образуют треугольник.
    # Самый "легкий" способ нарушить неравенство треугольника — 
    # взять две самые маленькие стороны и одну самую большую.
    # Если сумма двух самых маленьких (lst[0] и lst[1]) меньше или равна 
    # самому большому из доступных (lst[3]), то треугольник построить нельзя.
    # В коде используется строгое неравенство (<), так как в условии задачи
    # вырожденный треугольник (сумма равна третьей стороне) тоже считается
    # "не треугольником" в контексте задачи (или условие задачи подразумевает a+b < c).
    
    if lst[0] + lst[1] < lst[3]:
        count += 1

print(f'Ответ на задачу 1: {count}')


