f = open('data.txt')
result = [] # Список для хранения сумм подходящих строк

for line in f:
    lst = [int(i) for i in line.split()]
    
    # Формируем списки только четных и только нечетных чисел строки
    chet = [i for i in lst if i % 2 == 0]
    nechet = [i for i in lst if i % 2 != 0]
    
    # УСЛОВИЕ 1: Числа расположены в порядке возрастания.
    # Так как мы не сортировали lst, мы проверяем исходный порядок в файле.
    # Цепочка сравнений в Python работает как (a<b) and (b<c) and ...
    if lst[0] < lst[1] < lst[2] < lst[3] < lst[4] < lst[5]:
        
        # УСЛОВИЕ 2: Количество чётных и нечётных одинаково.
        # Для 6 чисел это значит 3 четных и 3 нечетных.
        if len(chet) == len(nechet):
            # Если оба условия выполнены, запоминаем сумму строки
            result.append(sum(lst))

# Так как мы читали файл последовательно (с 1-й строки до последней),
# в списке result значения лежат в порядке возрастания номеров строк.
# "Строка с наибольшим номером" — это последняя добавленная в список.
# result[-1] обращается к последнему элементу списка.
