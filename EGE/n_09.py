# Откройте файл электронной таблицы, содержащей в каждой строке три натуральных числа.
#
# Задание 9
#
# Выясните, какое количество троек чисел может являться сторонами треугольника,
# то есть удовлетворяет неравенству треугольника. В ответе запишите только число.

# Открываем файл с данными для чтения
f = open('data.txt')

# Инициализируем счётчик для подсчёта количества треугольников
count = 0

# СПОСОБ 1: Проверка неравенства треугольника через максимальную сторону
# Неравенство треугольника: каждая сторона должна быть меньше суммы двух других
# Достаточно проверить, что max(a,b,c) < (a+b+c) - max(a,b,c)
# Это эквивалентно: max(a,b,c) < сумма двух других сторон
for line in f:
    # Преобразуем строку в список целых чисел
    lst = list(map(int, line.split()))
    
    # Проверяем: максимальная сторона < сумма всех сторон - максимальная сторона
    # Т.е. максимальная сторона < сумма двух других сторон
    if max(lst) < sum(lst) - max(lst):
        count += 1

# СПОСОБ 2: Проверка неравенства треугольника через сортировку
# После сортировки достаточно проверить, что наибольшая сторона
# меньше суммы двух наименьших сторон
for line in f:
    # Преобразуем строку в список целых чисел
    lst = list(map(int, line.split()))
    
    # Сортируем стороны по возрастанию
    lst.sort()
    
    # Проверяем: самая большая сторона (последний элемент)
    # должна быть меньше суммы двух меньших сторон
    if lst[-1] < lst[0] + lst[1]:
        count += 1

# Выводим количество найденных треугольников
print(count)




# Откройте файл электронной таблицы, содержащей в каждой строке пять натуральных чисел.
#
# Определите количество строк таблицы, в которых квадрат суммы
# максимального и минимального чисел в строке больше суммы квадратов трёх оставшихся.

# Открываем файл с данными для чтения
f = open('data.txt')

# Инициализируем счётчик для подсчёта строк, удовлетворяющих условию
count = 0

# Обрабатываем каждую строку файла
for line in f:
    # Преобразуем строку в список целых чисел
    lst = list(map(int, line.split()))
    
    # Сортируем числа по возрастанию для удобного доступа к min и max
    # После сортировки: lst[0] - минимум, lst[4] - максимум
    lst.sort()
    
    # Проверяем условие: (min + max)² > (число2)² + (число3)² + (число4)²
    # lst[1], lst[2], lst[3] - три средних числа
    if (lst[0] + lst[4]) ** 2 > lst[1] ** 2 + lst[2] ** 2 + lst[3] ** 2:
        count += 1

# Выводим количество строк, удовлетворяющих условию
print(count)




# Откройте файл электронной таблицы, содержащей в каждой строке шесть натуральных чисел.
#
# Определите количество строк таблицы, содержащих числа, для которых выполнены оба условия:
#
# —в строке только одно число повторяется ровно два раза, остальные числа различны;
#
# —среднее арифметическое неповторяющихся чисел строки не больше суммы повторяющихся чисел.
#
# В ответе запишите только число.

# Открываем файл с данными для чтения
f = open('data.txt')

# Инициализируем счётчик для подсчёта строк, удовлетворяющих условиям
count = 0

# Обрабатываем каждую строку файла
for line in f:
    # Преобразуем строку в список целых чисел
    lst = list(map(int, line.split()))
    
    # Создаём список чисел, которые повторяются ровно 2 раза
    # list.count(i) возвращает количество вхождений элемента i в список
    povt = [i for i in lst if lst.count(i) == 2]
    
    # Создаём список чисел, которые встречаются только 1 раз (уникальные)
    nepovt = [i for i in lst if lst.count(i) == 1]
    
    # УСЛОВИЕ 1: Проверяем, что повторяющихся элементов ровно 2 (одно число 2 раза)
    # и все неповторяющиеся числа различны (нет дубликатов среди уникальных)
    # len(set(nepovt)) == len(nepovt) гарантирует отсутствие повторов среди уникальных
    if len(povt) == 2 and len(set(nepovt)) == len(nepovt):
        
        # УСЛОВИЕ 2: Проверяем, что среднее арифметическое неповторяющихся
        # не больше суммы повторяющихся чисел
        # Дополнительная проверка len(nepovt) != 0 защищает от деления на ноль
        if len(nepovt) != 0 and sum(nepovt) / len(nepovt) <= sum(povt):
            count += 1

# Выводим количество строк, удовлетворяющих обоим условиям
print(count)
