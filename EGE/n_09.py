# ЗАДАНИЕ: Работа с электронной таблицей
# =============================================================================
# В файле электронной таблицы Задание_9.xlsx в каждой строке записаны семь 
# натуральных чисел.
#
# Определите номер строки таблицы с наибольшей суммой чисел в строке, 
# для которой выполнены три условия:
#
# — в строке только одно число повторяется трижды, остальные четыре числа различны;
# — среднее арифметическое неповторяющихся чисел строки не больше повторяющегося числа;
# — максимальное число строки не кратно минимальному.
# =============================================================================

max_n = 0           # номер строки с максимальной суммой, удовлетворяющей условиям
max_sum = 0         # максимальная сумма чисел в подходящей строке

# Открываем файл с данными (предварительно экспортированным в текстовый формат)
with open('data.txt', 'r', encoding='utf-8') as f:
    # enumerate позволяет получить одновременно индекс строки и её содержимое
    for index, line in enumerate(f):
        # Преобразуем строку в список целых чисел
        lst = [int(i) for i in line.split()]
        
        # Находим числа, которые встречаются ровно 3 раза (повторяющееся)
        pov = [x for x in lst if lst.count(x) == 3]
        
        # Находим числа, которые встречаются ровно 1 раз (уникальные)
        nepov = [x for x in lst if lst.count(x) == 1]
        
        # Проверяем первое условие: 4 уникальных и 3 повторяющихся числа
        if len(nepov) == 4 and len(pov) == 3:
            # Проверяем второе условие: среднее уникальных ≤ повторяющемуся числу
            if sum(nepov) / len(nepov) <= pov[0]:
                # Проверяем третье условие: максимум не кратен минимуму
                if max(lst) % min(lst) != 0:
                    # Если все условия выполнены и сумма больше текущей макс. — обновляем
                    if sum(lst) > max_sum:
                        max_sum = sum(lst)
                        max_n = index + 1  # нумерация строк с 1, а не с 0

print(max_n)


