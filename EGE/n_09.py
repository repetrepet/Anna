# Откройте файл электронной таблицы, содержащей в каждой строке восемь натуральных чисел.
# Определите количество строк таблицы, содержащих числа, для которых выполнены оба условия:
#
# — в строке минимальное число встречается два или три раза, остальные числа без повторений;
#
# — сумма квадратов минимального и максимального из неповторяющихся чисел не больше квадрата суммы других неповторяющихся.
#
# В ответе запишите только число.

# Переоткрываем файл (или сбрасываем курсор f.seek(0), если файл тот же)
f = open('data.txt')
count = 0

for line in f:
    lst = [int(i) for i in line.split()]
    lst.sort() # Сортировка удобна для поиска минимума (он будет в начале)
    
    # 1. Формируем список повторяющихся чисел (pov), которые равны минимуму.
    # Условия: число встречается в строке 2 или 3 раза AND число является минимальным.
    pov = [x for x in lst if 2 <= lst.count(x) <= 3 and x == min(lst)]
    
    # 2. Формируем список неповторяющихся чисел (nepov).
    # Условие: число встречается в строке ровно 1 раз.
    nepov = [x for x in lst if lst.count(x) == 1]

    # ПРОВЕРКА ПЕРВОГО УСЛОВИЯ:
    # Если минимум повторяется 3 раза -> в списке nepov должно остаться 5 чисел (всего 8).
    # Если минимум повторяется 2 раза -> в списке nepov должно остаться 6 чисел (всего 8).
    # Примечание: len(pov) здесь будет равна количеству вхождений минимума (2 или 3), 
    # так как мы собрали все его экземпляры в список pov.
    
    if (len(nepov) == 5 and len(pov) == 3) or (len(nepov) == 6 and len(pov) == 2):
        
        # ПРОВЕРКА ВТОРОГО УСЛОВИЯ:
        # "сумма квадратов минимального и максимального из неповторяющихся..."
        val_min_nepov = min(nepov)
        val_max_nepov = max(nepov)
        sum_sq_min_max = val_min_nepov ** 2 + val_max_nepov ** 2
        
        # "...не больше квадрата суммы других неповторяющихся"
        # Чтобы найти сумму "других", берем общую сумму неповторяющихся и вычитаем 
        # из неё уже использованные минимум и максимум.
        sum_others = sum(nepov) - val_max_nepov - val_min_nepov
        
        if sum_sq_min_max <= sum_others ** 2:
            count += 1

print(f'Ответ на задачу 2: {count}')


