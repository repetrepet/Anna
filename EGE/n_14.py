# Укажите слитно (без пробелов и запятых) в порядке убывания все натуральные числа, меньшие 200,
# запись которых в четверичной системе счисления оканчивается на 123.
#
# Например, если условию удовлетворяют числа 37 и 45, то ответом будет строка 4537.

# 1_10 = 1_4
# 2_10 = 2_4

# 35_10 = x_4

def function(number):
    """Преобразует десятичное число в строку его записи в четверичной системе счисления"""
    result = ''
    # Алгоритм перевода: последовательно делим на 4, записывая остатки в обратном порядке
    while number != 0:
        result = str(number % 4) + result  # Остаток от деления — следующая цифра (справа налево)
        number //= 4  # Целочисленное деление на основание системы для перехода к следующему разряду
    return result


# Перебираем все натуральные числа меньше 200 в порядке убывания (от 199 до 1)
for i in range(199, 0, -1):
    # Проверяем, оканчивается ли четверичная запись числа на последние три цифры '123'
    # function(i) возвращает строку с четверичной записью, срез [-3:] берёт три последних символа
    if function(i)[-3:] == '123':
        print(i, end='')  # Выводим подходящее число без разделителей, end='' убирает перенос строки
