# ЗАДАЧА 2
# =============================================================================
# Значение арифметического выражения 9^8 + 3^5 − 9 записали в системе 
# счисления с основанием 3. Сколько цифр 2 содержится в этой записи?

# 💡 Пояснение:
# • Перевод числа в систему с основанием q осуществляется делением на q 
#   с записью остатков в обратном порядке
# • 9 = 3^2, поэтому выражение удобно представлять через степени тройки

x = 9 ** 8 + 3 ** 5 - 9
s = ''

while x != 0:
    s = str(x % 3) + s      # добавляем остаток от деления в начало строки
    x = x // 3              # целочисленное деление на основание системы

print(s.count('2'))         # подсчёт количества цифр «2» в троичной записи


# =============================================================================
# ЗАДАЧА 3
# =============================================================================
# Значение арифметического выражения 49^10 + 7^30 − 49 записали в системе 
# счисления с основанием 7. Сколько цифр 6 содержится в этой записи?

# 💡 Пояснение:
# • 49 = 7^2, значит 49^10 = (7^2)^10 = 7^20
# • Алгоритм перевода в систему счисления с основанием 7 аналогичен задаче 2

x = 49 ** 10 + 7 ** 30 - 49
s = ""

while x != 0:
    s = str(x % 7) + s      # формируем запись в семеричной системе
    x = x // 7

print(s.count("6"))         # подсчёт количества цифр «6» в записи


