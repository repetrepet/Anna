# ЗАДАЧА 1
# =============================================================================
# Сколько единиц содержится в двоичной записи значения выражения: 
# 4^2020 + 2^2017 – 15?

# 💡 Пояснение:
# • 4^2020 = (2^2)^2020 = 2^4040 — это единица в 4041-м разряде двоичной записи
# • 2^2017 — единица в 2018-м разряде
# • Вычитание 15 (1111₂) влияет на младшие разряды
# • bin(x) возвращает строку вида '0b101...', поэтому берём срез [2:]

x = 4 ** 2020 + 2 ** 2017 - 15
s = bin(x)[2:]              # перевод в двоичную систему, убираем префикс '0b'
print(s.count('1'))         # подсчёт количества единиц в строке


