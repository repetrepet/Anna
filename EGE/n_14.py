# ЗАДАЧА 3. Уравнение в системе счисления с переменным основанием p
# =============================================================================
# В системе счисления с основанием p выполняется равенство:
#     12_p · 34_p = xy2_p
# Буквами x и y обозначены некоторые цифры из алфавита системы счисления 
# с основанием p. Определите значение числа yx_p и запишите это значение 
# в десятичной системе счисления.

# Алфавит для систем счисления с основанием от 2 до 36:
# цифры 0-9 и заглавные латинские буквы A-Z (в верхнем регистре)
alphabet = sorted('0123456789QWERTYUIOPASDFGHJKLZXCVBNM')

# Перебираем возможные основания системы счисления
# Минимальное основание: 5 (т.к. в записи есть цифра '4')
# Максимальное основание: 36 (стандартное ограничение для int(..., base))
for p in range(5, 36):
    # Перебираем все допустимые цифры для x в алфавите системы с основанием p
    for x in alphabet[:p]:
        # Перебираем все допустимые цифры для y в алфавите системы с основанием p
        for y in alphabet[:p]:
            # Проверяем выполнение равенства: 12_p * 34_p = xy2_p
            # int(строка, p) — преобразование из p-ричной СС в десятичную
            if int('12', p) * int('34', p) == int(x + y + '2', p):
                # Найдено решение: выводим значение числа yx_p в десятичной СС
                print(int(y + x, p))


