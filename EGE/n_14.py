# ЗАДАЧА 5
# =============================================================================
# Значение арифметического выражения 5^2025 + 5^200 − x, где х — натуральное 
# число в диапазоне от 2 до 2025, записали в системе счисления с основанием 5. 
# Определите максимальное значение x, при котором данная запись содержит 
# наибольшее количество цифр «4».

# 💡 Пояснение:
# • Перебираем x в обратном порядке (от 2025 до 2), чтобы сразу найти 
#   максимальное подходящее значение
# • При вычитании малого x из большого числа, состоящего из степеней пятёрки,
#   в пятеричной записи возникают «четвёрки» за счёт заимствования в старших 
#   разрядах (аналогично 1000 - 1 = 999 в десятичной системе)
# • Отслеживаем максимум количества цифр «4» и соответствующее значение x

max_x = 0
max_fours = 0

for x in range(2025, 1, -1):          # перебор от большего к меньшему
    r = 5 ** 2025 + 5 ** 200 - x     # вычисляем значение выражения
    s = ''

    while r != 0:
        s = str(r % 5) + s            # перевод в пятеричную систему
        r //= 5

    current_fours = s.count('4')
    # Обновляем максимум: >= позволяет найти максимальное x при равном количестве четвёрок
    if current_fours >= max_fours:
        max_fours = current_fours
        max_x = x

print(max_x)                          # вывод ответа
