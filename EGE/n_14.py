# ЗАДАЧА 4
# =============================================================================
# 9 · 11^210 + 8 · 11^150 – x,
# где x — целое положительное число, не превышающее 3000, записали в 
# 11-ричной системе счисления. Определите наибольшее значение x, при котором 
# в 11-ричной записи числа, являющегося значением данного арифметического 
# выражения, содержится ровно 60 нулей.
# В ответе запишите число в десятичной системе счисления.

# 💡 Пояснение:
# • В 11-ричной системе цифры: 0,1,2,...,9,10 (где 10 — одна цифра!)
# • ⚠️ Внимание: при строковом представлении цифра «10» записывается как '10',
#   поэтому простой подсчёт '0' может дать ошибку (учитывает ноль внутри '10')
# • В данной реализации используется эвристика: k.count("0") - k.count('10')
#   для корректного учёта только «чистых» нулей

l = []

for x in range(1, 3001):
    r = 9 * 11 ** 210 + 8 * 11 ** 150 - x
    k = ""

    while r != 0:
        k = str(r % 11) + k   # перевод в 11-ричную систему
        r = r // 11

    # Подсчёт нулей с учётом особенности представления цифры «10»
    if k.count("0") - k.count('10') == 60:
        l.append(x)

print(max(l))                 # вывод максимального подходящего x


