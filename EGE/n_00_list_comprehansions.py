# ======================================================================================
# ТЕОРИЯ: List Comprehensions (списковые включения) в Python
# ======================================================================================
#
# List comprehension — это компактный способ создать новый список на основе:
# - перебора элементов (for),
# - фильтрации (if),
# - преобразования элемента (выражение слева),
# - (опционально) условного выражения (тернарный оператор),
# - (опционально) нескольких циклов for подряд.
#
# По смыслу list comprehension почти всегда эквивалентен циклу for с .append(),
# но читается короче и обычно работает быстрее за счёт оптимизаций интерпретатора.
#
# ------------------------------------------------------------------------------
# 1) БАЗОВЫЙ СИНТАКСИС
# ------------------------------------------------------------------------------
# [выражение for переменная in итерируемый_объект]
#
# Пример:
# squares = [x * x for x in range(5)]
# Это эквивалентно:
# squares = []
# for x in range(5):
#     squares.append(x * x)
#
# Где:
# - "выражение" — что кладём в итоговый список (может быть любым выражением)
# - "for x in ..." — откуда берём элементы
#
# ------------------------------------------------------------------------------
# 2) ФИЛЬТРАЦИЯ (IF В КОНЦЕ)
# ------------------------------------------------------------------------------
# [выражение for x in iterable if условие]
#
# Важно: if в конце — это фильтр (пропускает/не пропускает элемент).
# Пример:
# evens = [x for x in range(10) if x % 2 == 0]
#
# ------------------------------------------------------------------------------
# 3) УСЛОВНОЕ ВЫРАЖЕНИЕ (ТЕРНАРНЫЙ ОПЕРАТОР) ВНУТРИ
# ------------------------------------------------------------------------------
# [A if условие else B for x in iterable]
#
# Важно: это НЕ фильтр, а подстановка значения.
# Пример:
# sign = ["+" if x >= 0 else "-" for x in numbers]
#
# ------------------------------------------------------------------------------
# 4) И IF-ФИЛЬТР, И ТЕРНАРНЫЙ ОПЕРАТОР ВМЕСТЕ
# ------------------------------------------------------------------------------
# [A if cond1 else B for x in iterable if cond2]
#
# Сначала отработает фильтр (cond2), затем тернарное выражение (cond1).
#
# ------------------------------------------------------------------------------
# 5) НЕСКОЛЬКО ЦИКЛОВ FOR (ВЛОЖЕННОСТЬ)
# ------------------------------------------------------------------------------
# [(x, y) for x in X for y in Y]
#
# Эквивалент:
# result = []
# for x in X:
#     for y in Y:
#         result.append((x, y))
#
# Пример "сплющить список списков":
# flat = [item for row in matrix for item in row]
#
# ------------------------------------------------------------------------------
# 6) СЛОЖНЫЕ УСЛОВИЯ
# ------------------------------------------------------------------------------
# Можно писать любые логические выражения:
# [x for x in A if x % 3 == 0 and x % 5 != 0]
#
# ------------------------------------------------------------------------------
# 7) ВЫЗОВЫ ФУНКЦИЙ В ВЫРАЖЕНИИ
# ------------------------------------------------------------------------------
# [func(x) for x in data]
# Например: [str(x) for x in nums], [len(w) for w in words]
#
# ------------------------------------------------------------------------------
# 8) ВЗАИМОДЕЙСТВИЕ С СТРОКАМИ
# ------------------------------------------------------------------------------
# Часто используют split(), strip(), lower() и т.п.
# lengths = [len(word) for word in s.split()]
#
# ------------------------------------------------------------------------------
# 9) ПРОВЕРКА ТИПОВ И ФИЛЬТРАЦИЯ "ЛИШНИХ" ЭЛЕМЕНТОВ
# ------------------------------------------------------------------------------
# [x for x in B if isinstance(x, int)]
#
# Важный нюанс Python: bool — подкласс int, поэтому True/False тоже int.
# Чтобы исключить булевы значения:
# [x for x in B if isinstance(x, int) and not isinstance(x, bool)]
#
# ------------------------------------------------------------------------------
# 10) ПРОЧИЕ ВКЛЮЧЕНИЯ (ДЛЯ ОБЩЕГО ПОНИМАНИЯ)
# ------------------------------------------------------------------------------
# Set comprehension (множество):
# {x for x in data if cond}
#
# Dict comprehension (словарь):
# {key_expr: value_expr for x in data if cond}
#
# Generator expression (генератор, не хранит всё сразу в памяти):
# (expr for x in data if cond)
#
# ------------------------------------------------------------------------------
# 11) ЧИТАЕМОСТЬ И ПРАКТИЧЕСКИЕ СОВЕТЫ
# ------------------------------------------------------------------------------
# - Если выражение стало слишком длинным/запутанным — лучше обычный for.
# - Comprehension удобен для "собрать/преобразовать/отфильтровать" в один шаг.
# - Можно переносить на несколько строк, используя круглые/квадратные скобки.
#
# ======================================================================================


# однострочники

l1 = []

for i in range(10):
    # Условие фильтрации: берём только чётные числа
    if i % 2 == 0:
        # Добавляем подходящие элементы в список
        l1.append(i)

# list comprehension читается как:
# [что добавляем | где проходимся | какое условие]
l2 = [i for i in range(10) if i % 2 == 0]

print(l1)
print(l2)

# Пример: квадраты чётных чисел от 0 до 9
l = [i ** 2 for i in range(10) if i % 2 == 0]


# написать однострочник который собирает в список только четные числа до 10
l = [i for i in range(10) if i % 2 == 0]
print(l)

# Получи список квадратов чисел от 1 до 5.
# ВНИМАНИЕ: range(1, 5) даёт 1..4 (5 не включается), так работает range().
l = [i ** 2 for i in range(1, 5)]
print(l)

# Преобразуй список чисел в список строк.
l = [1, 2, 3, 4, 5, 6]
l = [str(i) for i in l]
print(l)

# Умножь каждый элемент списка на 2.
l = [1, 2, 3]
j = [i * 2 for i in l]

# Оставь только чётные числа из списка.
l = [1, 2, 3, 4, 5, 6]
j = [i for i in l if i % 2 == 0]

# Оставь числа больше 10.
l = [7, 8, 9, 10, 11, 12, 13]
j = [i for i in l if i > 10]

# Оставь строки длиной больше 3 символов.
l = ['abc', 'asdf', 'asdfghjk']
j = [i for i in l if len(i) > 3]

# Убери все None из списка.
l = [None, None, 12, 13, None]
# Лучше писать "is not None", но код не меняем — только поясняем.
j = [i for i in l if i != None]

# Получить список квадратов нечётных элементов списка A.
A = [3, 4, 5, 6, 7]
j = [i ** 2 for i in A if i % 2 != 0]

# Получить список элементов A, кратных 3, но не кратных 5.
A = [10, 12, 15, 18, 21]
j = [i for i in A if i % 3 == 0 and i % 5 != 0]

# Получить список элементов A,
# которые больше среднего арифметического элементов списка A.
A = [1, 2, 3, 4, 5, 6]
# Среднее: sum(A) / len(A). Сравниваем каждый элемент со средним.
j = [i for i in A if i > sum(A) / len(A)]


# Получить список значений True и False, где True означает,
# что соответствующий элемент списка A делится на 7.

A = [5, 6, 7, 8, 14, 15]
# Тернарный оператор внутри comprehension:
# если i делится на 7 -> True, иначе False.
j = [True if i % 7 == 0 else False for i in A]


# Заменить все отрицательные числа на 0.
l = [-1, -2 , 4, 5, 6, -1]
# Здесь нет фильтрации: длина списка сохраняется, меняются только значения.
j = [0 if i < 0 else i for i in l]


# Преобразовать список так,
# чтобы чётные числа заменялись на их квадраты, а нечётные — на их кубы.

l = [1, 2, 3, 4, 5]
j = [i ** 2 if i % 2 == 0 else i ** 3 for i in l]

# Получить список длин всех слов строки S.
s = 'привет мой друг'
# s.split() разбивает строку по пробелам на слова.
j = [len(i) for i in s.split()]

# Получить список слов строки S, длина которых больше 4.
j = [i for i in s.split() if len(i) > 4]

# Получить список слов, начинающихся с гласной буквы.
s = 'арбуз апельсин яблоко вищня'
vowels = 'аияоуеюёэы'
# i[0] — первая буква слова; проверяем принадлежность множеству гласных.
j = [i for i in s.split() if i[0] in vowels]

# Получить список целых чисел из списка B, игнорируя элементы, которые не являются числами.

B = ['ads', 23, 234, True, [12, 23, 45], 'asdasd', 66, 57]
# bool в Python является подклассом int, поэтому отдельно исключаем bool.
j = [i for i in B if isinstance(i, int) and not isinstance(i, bool)]

# Сформировать список квадратов всех чисел от 1 до 100, оканчивающихся на 3.
# str(i)[-1] берёт последнюю цифру числа в виде символа.
j = [i ** 2 for i in range(1, 101) if str(i)[-1] == '3']

# Сформировать список всех двузначных чисел, сумма цифр которых равна 7.
# i // 10 — десятки, i % 10 — единицы.
j = [i for i in range(10, 100) if (i // 10) + (i % 10) == 7]

# Сформировать список чисел от 1 до 200, не содержащих цифру 5.
# Условие '5' not in str(i) отсекает все числа, где встречается цифра 5.
j = [i for i in range(1, 201) if '5' not in str(i)]

print(j)
