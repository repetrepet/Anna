# Обозначим через ДЕЛ(n, m) утверждение «натуральное число n делится без остатка на натуральное число m». Для какого наименьшего натурального числа A логическое выражение
# (не ДЕЛ(x, 26) и ДЕЛ(x, A)) -> (ДЕЛ(x, 39) или не ДЕЛ(x, A))
# тождественно истинно (т. е. принимает значение 1 при любом неотрицательном целом значении переменной x)?

def f(n, m):
    # Реализация операции ДЕЛ(n, m): возвращает True, если n кратно m, и False в противном случае
    return n % m == 0

# Перебираем возможные значения параметра A, начиная с 1.
# Верхняя граница диапазона взята с запасом, чтобы гарантированно охватить искомое значение.
for A in range(1, 1000):
    # Функция all() проверяет, выполняется ли логическое выражение для КАЖДОГО x в заданном диапазоне.
    # Если хотя бы для одного x выражение ложно, all() вернёт False, и проверка текущего A прервётся.
    if all(
        # В Python логическая импликация (P -> Q) заменяется на сравнение P <= Q.
        # Это корректно, так как в булевой алгебре False (0) <= True (1) и True (1) <= True (1) дают True,
        # а единственный ложный случай True (1) <= False (0) даёт False, что совпадает с таблицей истинности импликации.
        (not f(x, 26) and f(x, A)) <= (f(x, 39) or (not f(x, A)))
        for x in range(0, 1000)
    ):
        # Как только находим первое A, при котором выражение истинно для всех x из диапазона,
        # выводим его. Поскольку цикл идёт от меньших чисел к большим, результат гарантированно минимальный.
        print(A)
        # Прерываем поиск, так как наименьшее подходящее значение уже найдено.
        break


