# На числовой прямой даны отрезки: P = [7; 16], Q = [25; 32].
# Укажите наименьшую длину такого отрезка A,
# для которого логическое выражение
#
# ((x ∈ P) → (x ∈ A)) ∨ (¬(x ∈ Q) → (x ∈ A))
#
# истинно (т.е. принимает значение 1) при любом значении переменной x.

P = range(7, 17)   # Отрезок P: [7; 16]
Q = range(25, 33)  # Отрезок Q: [25; 32]
A = []             # Список точек, которые должны войти в искомый отрезок A

# Перебираем целые значения x в достаточном диапазоне
for x in range(-100, 100):
    # Выражение должно быть ИСТИННО для всех x.
    # Если для конкретного x оно ЛОЖНО, условие задачи нарушается,
    # следовательно, этот x обязательно должен принадлежать отрезку A.
    # В Python оператор <= для булевых значений эквивалентен импликации (→)
    if not (((x in P) <= (x in A)) or ((x not in Q) <= (x in A))):
        A.append(x)

# Наименьшая длина отрезка A равна разности между его границами
print(A[-1] - A[0])


# На числовой прямой даны два отрезка: P = [19; 142] и Q = [75; 185].
# Укажите наименьшую возможную длину такого отрезка A, для которого логическое выражение
#
# ¬((x ∈ Q) → ((¬(x ∈ A) ∧ (x ∈ P)) → ¬(x ∈ Q)))
#
# ложно (т.е. принимает значение 0) при любом значении переменной x.

p = range(19, 143)  # Отрезок P: [19; 142]
q = range(75, 186)  # Отрезок Q: [75; 185]
a = []              # Точки, формирующие отрезок A

for x in range(-100, 1000):
    # Выражение должно быть ЛОЖНО для любого x.
    # Проверяем исходное условие: если оно не ложно (т.е. истинно),
    # то x нарушает требование задачи и должен быть включён в A.
    if not (not ((x in q) <= ((x not in a) and (x in p) <= (x not in q)))) == False:
        a.append(x)

print(a)


# На числовой прямой даны три отрезка: P = [10; 150], Q = [160; 250] и R = [240; 300].
# Укажите наименьшую возможную длину такого отрезка A,
# для которого логическое выражение
# ((x ∈ Q) → (x ∈ P)) ∨ (¬(x ∈ A) → (x ∈ R))
# истинно (т.е. принимает значение 1) при любом значении переменной x.

p = range(10, 151)   # Отрезок P: [10; 150]
q = range(160, 251)  # Отрезок Q: [160; 250]
r = range(240, 301)  # Отрезок R: [240; 300]
a = []               # Точки отрезка A

for x in range(500):
    # Проверяем выполнение условия.
    # Если дизъюнкция оказывается ложной, требование "истинно при любом x" нарушается,
    # поэтому такой x добавляем в список обязательных точек для отрезка A.
    if not (
        ((x in q) <= (x in p)) or ((x not in a) <= (x in r))
    ):
        a.append(x)

print(a)


