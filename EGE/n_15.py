# На числовой прямой даны отрезки: P = [7; 16], Q = [25; 32].
# Укажите наименьшую длину такого отрезка A,
# для которого логическое выражение
#
# ((x ∈ P) → (x ∈ A)) ∨ (¬(x ∈ Q) → (x ∈ A))
#
# истинно (т.е. принимает значение 1) при любом значении переменной x.

P = range(7, 17)   # Отрезок P: [7; 16]
Q = range(25, 33)  # Отрезок Q: [25; 32]
A = []             # Список точек, которые должны войти в искомый отрезок A

# Перебираем целые значения x в достаточном диапазоне
for x in range(-100, 100):
    # Выражение должно быть ИСТИННО для всех x.
    # Если для конкретного x оно ЛОЖНО, условие задачи нарушается,
    # следовательно, этот x обязательно должен принадлежать отрезку A.
    # В Python оператор <= для булевых значений эквивалентен импликации (→)
    if not (((x in P) <= (x in A)) or ((x not in Q) <= (x in A))):
        A.append(x)

# Наименьшая длина отрезка A равна разности между его границами
print(A[-1] - A[0])


