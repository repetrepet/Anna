# На числовой прямой даны два отрезка: B = [22; 40] и C = [32; 50]. Укажите наименьшую возможную длину такого отрезка A, для которого логическое выражение

# не (x ∈ A) → ((x ∈ B) ≡ (x ∈ C))

# истинно (т. е. принимает значение 1) при любом значении переменной x.

# Задаём диапазоны целых чисел для отрезков B и C. 
# В Python range(start, stop) не включает правую границу, поэтому к ней прибавляем 1.
B = range(22, 40 + 1)
C = range(32, 50 + 1)

# Список A будет накапливать значения x, при которых исходное выражение ложно.
# Чтобы условие задачи выполнялось (выражение истинно при любом x), 
# отрезок A обязан покрывать все такие "проблемные" точки.
A = []

# Перебираем x в широком диапазоне, чтобы гарантированно захватить все области пересечения и разности отрезков.
for x in range(0, 100):
    # Переводим логические операции в синтаксис Python:
    # импликация (→) заменяется на оператор <=,
    # эквивалентность (≡) — на оператор ==,
    # отрицание (¬ или "не") — на оператор not.
    # Проверяем условие задачи. Если оно возвращает False, значит, без x в отрезке A выражение нарушается.
    if not(
        ((not (x in A)) <= ((x in B) == (x in C)))   
    ):
        # Фиксируем найденное "проблемное" значение. 
        # Включение x в A сделает часть "не (x ∈ A)" ложной, что автоматически обратит импликацию в истину.
        A.append(x)

# Длина отрезка определяется разностью между его максимальной и минимальной координатой.
# Поскольку x перебирались по возрастанию, список A уже отсортирован.
print(A[-1] - A[0])



