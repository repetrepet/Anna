# Для какого наибольшего неотрицательного целого числа A формула

# (x & 52 = 4) -> ((x & 26 = 2) -> (x & A = 6))

# тождественно истинна (т.е. принимает значение 1 при любом неотрицательном целом значении переменной x)?

# Перебираем возможные значения A в диапазоне, покрывающем все значимые разряды.
# Так как цикл идёт по возрастанию, программа выведет все подходящие A подряд,
# а последнее напечатанное число автоматически окажется наибольшим.
for A in range(1, 1000):
    # Функция all() проверяет, выполняется ли условие для каждого x из диапазона.
    # В Python логическая импликация (->) заменяется оператором <=, потому что
    # булевы значения True и False являются подклассом int (1 и 0 соответственно).
    # Выражение (P <= Q) возвращает False только когда P=True, а Q=False,
    # что полностью совпадает с таблицей истинности для импликации P -> Q.
    # Сравнение с 1 гарантирует, что итоговый результат цепочки импликаций равен True.
    if all(
        ((x & 52 == 4) <= ((x & 26 == 2) <= (x & A == 6))) == 1
        for x in range(0, 1000)
    ):
        # Если формула тождественно истинна на всём проверенном диапазоне x,
        # текущее значение A удовлетворяет условию задачи.
        print(A)
