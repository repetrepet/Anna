# тождественно истинна (то есть принимает значение 1 при любом натуральном значении переменной x)?

def f(n, m):
    return n % m == 0

for A in range(1, 100000):
    # Формула представляет собой конъюнкцию (логическое И): оба условия должны выполняться одновременно.
    # 1. Импликация делимости: если x делится на A и на 36, он обязан делиться на 324.
    # 2. Прямое ограничение на параметр: само число A должно быть строго больше 100.
    if all(
        ((f(x, A) and f(x, 36)) <= f(x, 324)) and (A > 100)
        for x in range(1, 100000)
    ):
        print(A)
        break


# Обозначим через ДЕЛ(n, m) утверждение «натуральное число n делится без остатка на натуральное число m».
# Например, ДЕЛ(21, 3) = ИСТИНА, потому что 21 делится на 3 нацело. ДЕЛ(20, 7) = ЛОЖЬ, так как 20 не делится на 7 нацело.
# Найдите минимальное значение A, для которого приведённое выражение истинно, то есть принимает значение 1 для любого натурального значения x.
# не ДЕЛ(x, A) или не ДЕЛ(x, 2205) или ДЕЛ(x, 2800)

def f(n, m):
    return n % m == 0

for A in range(1, 1000000):
    # Ищем минимальное A, при котором дизъюнкция (ИЛИ) всегда истинна для любого натурального x.
    # Выражение станет ложным только в одном случае: когда все три условия одновременно ложны,
    # то есть когда x делится на A, x делится на 2205 и x НЕ делится на 2800.
    # Подбирая A, мы должны исключить возможность возникновения этой ситуации.
    if all(
        ((not f(x, A)) or (not f(x, 2205)) or f(x, 2800))
        for x in range(1, 1000000)
    ):
        print(A)
        break


