# Какая строка получится в результате применения приведённой ниже программы к строке, состоящей
# из 68 идущих подряд цифр 8? В ответе запишите полученную строку.
#
#  НАЧАЛО
#
#  ПОКА нашлось (222) ИЛИ нашлось (888)
#
#     ЕСЛИ нашлось (222)
#
#         ТО заменить (222, 8)
#
#         ИНАЧЕ заменить (888, 2)
#
#     КОНЕЦ ЕСЛИ
#
#  КОНЕЦ ПОКА
#
#  КОНЕЦ

# Решение на Python:
# Инициализируем строку из 68 восьмёрок
s = '8' * 68

# Цикл работает, пока в строке есть подстрока '222' или '888'
# Оператор 'in' проверяет вхождение подстроки (аналог "нашлось")
while ('222' in s) or ('888' in s):
    # Если есть '222', заменяем первое вхождение на '8'
    if '222' in s:
        # replace(old, new, count) — третий аргумент ограничивает количество замен
        # В задачах на исполнителя обычно заменяется первое найденное вхождение
        s = s.replace('222', '8', 1)
    # Иначе (если '222' нет, но есть '888' из-за условия цикла)
    else:
        s = s.replace('888', '2', 1)

print(f"Ответ к задаче 1: {s}")


# -----------------------------------------------------------------------------
# ЗАДАЧА 2
# -----------------------------------------------------------------------------
# Ниже приведена программа для исполнителя Редактор.
#
# НАЧАЛО
#
#  ПОКА нашлось (19) ИЛИ нашлось (299) ИЛИ нашлось (3999)
#
#   заменить (19, 2)
#   заменить (299, 3)
#   заменить (3999, 1)
#
#  КОНЕЦ ПОКА
#
# КОНЕЦ
#
# На вход этой программе подаётся строка длины 99, состоящая из цифры 1, за которой следуют 98
# идущих подряд цифр 9. Какая строка получится в результате применения программы к этой строке? В
# ответе запишите полученную строку.

# Решение на Python:
# Формируем исходную строку: '1' + 98 раз цифра '9'
s = '1' + '9' * 98

# Цикл продолжается, пока выполняется хотя бы одно из условий наличия подстрок
while ('19' in s) or ('299' in s) or ('3999' in s):
    # Последовательное выполнение команд замены внутри цикла
    # Важно: замены выполняются по очереди за одну итерацию цикла
    s = s.replace('19', '2', 1)
    s = s.replace('299', '3', 1)
    s = s.replace('3999', '1', 1)

print(f"Ответ к задаче 2: {s}")


# -----------------------------------------------------------------------------
# ЗАДАЧА 3
# -----------------------------------------------------------------------------
# Какая строка получится в результате применения приведённой ниже программы к строке вида 1…12…2
# (40 единиц и 40 двоек)?
#
# НАЧАЛО
#
# ПОКА нашлось (111)
#
#     заменить (111, 2)
#
#     заменить (222, 1)
#
# КОНЕЦ ПОКА
#
# КОНЕЦ

# Решение на Python:
# Формируем строку: 40 единиц followed by 40 двоек
s = '1' * 40 + '2' * 40

# Цикл работает, пока в строке есть тройка единиц '111'
while '111' in s:
    # Сначала заменяем первое вхождение '111' на '2'
    s = s.replace('111', '2', 1)
    # Затем сразу же проверяем и заменяем первое вхождение '222' на '1'
    # Это происходит внутри той же итерации цикла
    s = s.replace('222', '1', 1)

print(f"Ответ к задаче 3: {s}")
