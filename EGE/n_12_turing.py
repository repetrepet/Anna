# Дата: 21 мая 2026 г.
# На ленте исполнителя МТ в соседних ячейках записана последовательность из
# 1000
# 1000 символов, состоящей из
# 323 двоек,
# 115 нулей и
# 562 единиц, расположенных в указанном порядке. Ячейки справа и слева от последовательности заполнены пустыми символами «
# λ». В начальный момент времени головка расположена в ближайшей ячейке слева от последовательности. Программа для исполнителя:

# Формируем исходную строку в точном соответствии с условием:
# сначала 323 двойки, затем 115 нулей, затем 562 единицы.
x = '2' * 323 + '0' * 115 + '1' * 562

# Цикл проходит по каждому символу исходной последовательности.
# Важный технический момент: итератор в Python создаётся до начала цикла по объекту строки.
# Поскольку строки в Python неизменяемы, присваивание x = x.replace(...) создаёт новый объект,
# а итератор продолжает обращаться к исходной строке. 
# Это гарантирует, что цикл выполнит ровно 1000 шагов в изначальном порядке,
# независимо от изменений текущей ленты.
for i in x:
    # Если на текущем шаге исходной последовательности встречен '0',
    # заменяем первое вхождение '0' в текущей строке на '2'.
    # Третий аргумент 1 ограничивает замену только одним (самым левым) символом.
    if i == '0':
        x = x.replace('0', '2', 1)
        
    # Если встречена '1', заменяем первую единицу на ноль.
    # Команда break немедленно прерывает цикл, что моделирует команду остановки исполнителя.
    elif i == '1':
        x = x.replace('1', '0', 1)
        break
        
    # Если встречена '2', заменяем первую двойку на единицу.
    elif i == '2':
        x = x.replace('2', '1', 1)

# Выводим итоговое состояние ленты после завершения работы программы.
print(x)

# Вычисляем сумму всех цифр в итоговой строке:
# 1. Списковое включение [int(i) for i in x] последовательно преобразует каждый символ в целое число.
# 2. Функция sum() складывает элементы полученного списка.
# 3. Результат выводится на экран.
print(sum([int(i) for i in x]))
