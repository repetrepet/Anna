from turtle import *          # Импортируем все функции из модуля turtle для рисования

screensize(3000,3000)         # Устанавливаем размер экрана (холста) 3000x3000 пикселей
left(90)                      # Поворачиваем черепаху влево на 90 градусов (смотрит вверх)
tracer(0)                     # Отключаем анимацию для быстрого прорисовывания фигур
m = 30                        # Вводим переменную масштаба (единица измерения = 30 пикселей)

pendown()                     # Опускаем перо — черепаха будет рисовать

for i in range(2):            # Рисуем прямоугольник 9m x 15m
    fd(9 * m)                 # Двигаемся вперёд на 9*m пикселей
    rt(90)                    # Поворачиваем направо на 90 градусов
    fd(15 * m)                # Двигаемся вперёд на 15*m пикселей
    rt(90)                    # Поворачиваем направо на 90 градусов

penup()                       # Поднимаем перо — чтобы не рисовать линию при перемещении

fd(12 * m)                    # Двигаемся вперёд на 12*m пикселей
rt(90)                        # Поворачиваем направо на 90 градусов

pendown()                     # Опускаем перо — снова рисуем

for i in range(2):            # Рисуем второй прямоугольник 6m x 12m
    fd(6 * m)                 # Вперёд на 6*m пикселей
    rt(90)                    # Направо на 90 градусов
    fd(12 * m)                # Вперёд на 12*m пикселей
    rt(90)                    # Направо на 90 градусов

penup()                       # Поднимаем перо — далее будем ставить точки

# Рисуем сетку из синих точек
for x in range(-20, 20):      # Перебираем x-координаты от -20 до 19
    for y in range(-20, 20):  # Перебираем y-координаты от -20 до 19
        setpos(x * m, y * m)  # Перемещаемся в точку (x*m, y*m)
        dot(3, 'blue')        # Ставим синюю точку диаметром 3 пикселя

done()                        # Завершаем выполнение программы (окно остаётся открытым)

