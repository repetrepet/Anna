# В файле содержится последовательность целых чисел. Элементы последовательности могут принимать целые значения от –10 000 до 10 000 включительно.
# Определите и запишите в ответе сначала количество пар элементов последовательности, в которых разность элементов чётна и кратна 37, затем максимальную из сумм элементов таких пар. В данной задаче под парой подразумевается два идущих подряд элемента последовательности.

# Считываем последовательность из файла в список.
f = open('data.txt')
lst = [int(i) for i in f]

# Создаем пустой список для хранения сумм элементов подходящих пар.
# Это позволит позже легко найти максимальную сумму с помощью функции max().
sums = []

# Счетчик количества подходящих пар.
count = 0

# Проходим по всем возможным парам соседних элементов.
for index in range(len(lst) - 1):
    n1 = lst[index]
    n2 = lst[index + 1]
    
    # Вычисляем разность элементов. 
    # Проверяем два условия одновременно: разность должна быть чётной (% 2 == 0) 
    # и кратной 37 (% 37 == 0).
    if (n2 - n1) % 2 == 0 and (n2 - n1) % 37 == 0:
        # Если условия выполнены, добавляем сумму этой пары в список sums.
        sums.append(n1 + n2)
        # Увеличиваем счетчик подходящих пар.
        count += 1

# Выводим количество найденных пар и максимальную сумму среди них.
# Функция max() корректно работает, так как мы гарантированно добавляли элементы в список sums.
print(count, max(sums))


# В файле содержится последовательность натуральных чисел. Её элементы могут принимать целые значения от 1 до 100 000 включительно.
# Определите количество пар последовательности, в которых хотя бы один из элементов является четырёхзначным числом, a сумма элементов пары кратна минимальному трёхзначному элементу последовательности, кратному 45.
# В ответе запишите количество найденных пар, затем минимальную из сумм элементов таких пар. В данной задаче под парой подразумевается два идущих подряд элемента последовательности.

# Считываем данные из файла в список.
f = open('data.txt')
lst = [int(i) for i in f]

# Сначала находим все трёхзначные числа (от 100 до 999 включительно), которые кратны 45.
# Фильтруем исходный список с помощью генератора.
elements = [i for i in lst if 100 <= i <= 999 and i % 45 == 0]

# Находим минимальный элемент среди отобранных. 
# Это значение будет использоваться как делитель для проверки сумм пар.
element = min(elements)

# Список для хранения сумм подходящих пар и счетчик таких пар.
sums = []
count = 0

# Перебираем все пары соседних элементов в последовательности.
for index in range(len(lst) - 1):
    n1 = lst[index]
    n2 = lst[index + 1]
    
    # Проверяем первое условие: хотя бы одно из чисел в паре должно быть четырёхзначным (от 1000 до 9999).
    if (1000 <= n1 <= 9999) or (1000 <= n2 <= 9999):
        # Проверяем второе условие: сумма элементов пары должна быть кратна найденному минимальному элементу.
        if (n1 + n2) % element == 0:
            # Если оба условия выполнены, сохраняем сумму и увеличиваем счетчик.
            sums.append(n1 + n2)
            count += 1

# Выводим количество найденных пар и минимальную сумму среди них.
print(count, min(sums))


# В файле содержится последовательность целых чисел, абсолютное значение которых не превышает 10000.
# Определите количество троек элементов последовательности, в которых есть элемент, оканчивающийся на 11. 
# При этом среднее арифметическое тройки должно быть больше среднего арифметического из всех элементов последовательности, кратных 11.
# В ответе запишите количество троек чисел, а затем максимальную из сумм элементов таких троек.
# В этой задаче под тройкой подразумеваются три идущих подряд элемента последовательности.

# Считываем последовательность чисел из файла.
f = open('data.txt')
lst = [int(i) for i in f]

# Находим все элементы последовательности, которые кратны 11.
elements = [i for i in lst if i % 11 == 0]

# Вычисляем среднее арифметическое этих элементов.
# Деление суммы элементов на их количество дает эталонное значение для сравнения.
avg_11 = sum(elements) / len(elements)

# Список для сумм подходящих троек и счетчик таких троек.
sums = []
count = 0

# Проходим циклом по индексам до len(lst) - 2, чтобы можно было взять три элемента: index, index + 1 и index + 2.
for index in range(len(lst) - 2):
    # Распаковываем три подряд идущих элемента для удобства чтения кода.
    n1, n2, n3 = lst[index], lst[index + 1], lst[index + 2]
    
    # Проверяем, оканчивается ли хотя бы одно из чисел на 11.
    # Используем abs(), чтобы корректно обрабатывать отрицательные числа 
    # (например, для -111 остаток от деления на 100 в Python равен 89, а abs(-111) % 100 даст 11).
    if abs(n1) % 100 == 11 or abs(n2) % 100 == 11 or abs(n3) % 100 == 11:
        # Проверяем второе условие: среднее арифметическое тройки должно быть строго больше avg_11.
        # Для оптимизации можно сравнивать суммы: (n1 + n2 + n3) / 3 > avg_11 эквивалентно (n1 + n2 + n3) > avg_11 * 3, 
        # но оставляем исходную логику с делением для наглядности соответствия условию.
        if (n1 + n2 + n3) / 3 > avg_11:
            # Если условия выполнены, добавляем сумму тройки в список и увеличиваем счетчик.
            sums.append(n1 + n2 + n3)
            count += 1

# Выводим итоговое количество троек и максимальную сумму среди них.
print(count, max(sums))
