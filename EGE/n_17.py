# В файле содержится последовательность целых чисел. Элементы последовательности могут принимать целые значения от –10 000 до 10 000 включительно.
# Найдите количество пар чисел с одинаковым знаком. Под парой понимается 2 подряд идущих числа.

# Открываем файл для чтения. Предполагается, что файл 'data.txt' находится в той же директории.
f = open('data.txt')

# Считываем все строки из файла, преобразуем каждую в целое число и сохраняем в список.
# Генератор списка позволяет сделать это компактно и эффективно.
lst = [int(i) for i in f]

# Инициализируем счетчик подходящих пар нулем.
count = 0

# Проходим циклом по индексам списка до предпоследнего элемента.
# Это гарантирует, что при обращении к index + 1 мы не выйдем за границы списка.
for index in range(len(lst) - 1):
    # Извлекаем текущий элемент и следующий за ним элемент, образуя пару.
    n1 = lst[index]
    n2 = lst[index + 1]
    
    # Проверяем условие одинакового знака: 
    # либо оба числа неотрицательные (>= 0), либо оба строго отрицательные (< 0).
    if (n1 >= 0 and n2 >= 0) or (n1 < 0 and n2 < 0):
        # Если условие выполняется, увеличиваем счетчик найденных пар на 1.
        count += 1

# Выводим итоговое количество пар, удовлетворяющих условию.
print(count)


