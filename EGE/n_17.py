f = open('data.txt')
lst = [int(i) for i in f]

# Находим все элементы последовательности, которые кратны 11.
elements = [i for i in lst if i % 11 == 0]

# Вычисляем среднее арифметическое этих элементов.
# Деление суммы элементов на их количество дает эталонное значение для сравнения.
avg_11 = sum(elements) / len(elements)

