# Под парой в задаче подразумевается два подряд идущих числа. Первая и последняя пара в файле не могут быть искомыми, так как перед или после них нет чисел.

# Идея решения: Так как пара не может быть первой или последней, мы рассматриваем элементы с индексами от 1 до len(lst)-2. Для каждой такой пары сравниваем её произведение с произведением элементов, стоящих непосредственно перед и после неё. Если произведение пары строго больше, мы добавляем её сумму в список. Параллельно считаем количество пар, где хотя бы один элемент больше среднего арифметического всех чисел.

f = open('data.txt')
lst = [int(i) for i in f]
count = 0
sums = []

# Перебираем индексы так, чтобы у пары (index+1, index+2) были соседи (index и index+3).
# Поэтому идём до len(lst) - 3
for index in range(len(lst) - 3):
    # Элементы самой пары
    n1 = lst[index + 1] 
    n2 = lst[index + 2]
    # Элементы, стоящие перед парой и после неё
    m1 = lst[index]
    m2 = lst[index + 3]
    
    # Проверяем условие: произведение элементов пары строго больше произведения соседних элементов
    if (n1 * n2) > (m1 * m2):
        sums.append(n1 + n2)
        
        # Считаем среднее арифметическое всех чисел в файле
        avg = sum(lst) / len(lst)
        # Проверяем, есть ли в паре хотя бы одно число, большее среднего.
        # Складываем логические выражения и проверяем, что сумма >= 1
        if (n1 > avg) + (n2 > avg) >= 1:
            count += 1

# Выводим максимальную сумму среди подходящих пар и количество пар с элементом больше среднего
