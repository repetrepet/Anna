# Пример 1: Инверсия битов
def invert_bits(n):
    binary = bin(n)[2:].zfill(8)  # Дополняем до 8 бит
    inverted = binary.replace('0', '*').replace('1', '0').replace('*', '1')
    return int(inverted, 2)

print(f"Инверсия 13: {invert_bits(13)}")  # 242

# Пример 2: Обработка трехзначного числа
def process_three_digit(n):
    s = str(n)
    sum1 = int(s[0]) + int(s[1])
    sum2 = int(s[1]) + int(s[2])
    return str(max(sum1, sum2)) + str(min(sum1, sum2))

print(f"Обработка 348: {process_three_digit(348)}")  # 127

# Пример 3: Обработка четырехзначного числа
def process_four_digit(n):
    s = str(n)
    sums = [
        int(s[0]) + int(s[1]),
        int(s[1]) + int(s[2]),
        int(s[2]) + int(s[3])
    ]
    sums.sort()
    return str(sums[1]) + str(sums[2])

print(f"Обработка 1984: {process_four_digit(1984)}")  # 1217
