# ==============================================================
# Программа для поиска максимального числа, кратного 5
# ==============================================================

n = int(input("Введите количество чисел: "))  # Запрос количества чисел
l = []  # Список для хранения чисел, кратных 5

# Ввод чисел и фильтрация кратных 5
for i in range(n):
    number = int(input("Введите число: "))
    if number % 5 == 0:
        l.append(number)

print(max(l))  # Вывод максимального числа, кратного 5

