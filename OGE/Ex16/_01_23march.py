# ==============================================================
# Программа для вычисления суммы чисел, кратных 6 и оканчивающихся на 4
# ==============================================================

l = []

while True:
    number = int(input("Введите число: "))
    if number == 0:
        break
    if number % 6 == 0 and number % 10 == 4:
        l.append(number)

print(sum(l))  # Вывод суммы чисел, кратных 6 и оканчивающихся на 4

# ==============================================================
# Программа для нахождения сумм двух наибольших и двух наименьших чисел
# ==============================================================

l = []

while True:
    number = int(input("Введите число: "))
    if number == 0:
        break
    l.append(number)

l.sort()  # Сортировка списка
print(l[-1] + l[-2])  # Сумма двух наибольших чисел
print(l[0] + l[1])  # Сумма двух наименьших чисел

