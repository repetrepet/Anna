# Индексы и срезы

# Индекс - это порядковый номер в списке/строке и т.д.
# Пример списка фруктов с индексами:
#           0          1         2           3
fruits = ['apple', 'banana', 'cherry', 'watermelon']

# Выводим весь список фруктов
print(fruits)

# Выводим фрукт с индексом 2 (третий элемент)
print(fruits[2])  # Вывод: cherry

# Выводим фрукт с индексом 3 (четвертый элемент)
print(fruits[3])  # Вывод: watermelon

# Выводим фрукт с индексом 0 (первый элемент)
print(fruits[0])  # Вывод: apple

# Заменяем фрукт с индексом 0 на 'peach'
fruits[0] = 'peach'
# Выводим обновленный список фруктов
print(fruits)  # Вывод: ['peach', 'banana', 'cherry', 'watermelon']

# Переставляем фрукты с индексами 0 и 3
fruits = ['apple', 'banana', 'cherry', 'watermelon']
fruits[0], fruits[3] = fruits[3], fruits[0]
# Выводим обновленный список фруктов
print(fruits)  # Вывод: ['watermelon', 'banana', 'cherry', 'apple']

# Пример строки с индексами:
#    01234567...
s = 'Hello, world!'

# Выводим символ с индексом 1 (второй символ)
print(s[1])  # Вывод: e

# Выводим символ с индексом 7 (восьмой символ)
print(s[7])  # Вывод: w

